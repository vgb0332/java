package hufs.ces.dirtree;

import java.io.File;

import javax.swing.tree.DefaultTreeModel;

public class DirTreeModel {

	public DirTreeView view = null;
	
	private File selectedFile = null;
	
	private DefaultTreeModel dirTree = null;
	
	public DirTreeModel(DirTreeView view){
		this.view = view;
	}

	public File getSelectedFile() {
		return selectedFile;
	}

	public void setSelectedFile(File selectedFile) {
		this.selectedFile = selectedFile;
	}

	public DefaultTreeModel getDirTree() {
		return dirTree;
	}

	public void setDirTree(DefaultTreeModel dirTree) {
		this.dirTree = dirTree;
	}
}
