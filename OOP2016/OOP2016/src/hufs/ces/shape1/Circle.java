package hufs.ces.shape1;

public class Circle extends Shape {
	double radius;
	public Circle(double r) {
		radius = r;
	}
	public Circle(int r) {
		radius = r;
	}
	public String toString() {
		return "Circle of  Radius " + radius;
	}
	
	public double computeArea() {
		return Math.PI * radius * radius;
	}
}
